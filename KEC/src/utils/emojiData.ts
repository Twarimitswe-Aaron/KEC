// Get all emojis flattened
export const getAllEmojis = (): string[] => {
  return Object.values(emojiCategories).flat();
};

// Get category names
export const getCategoryNames = (): string[] => {
  return Object.keys(emojiCategories);
};

// Search emojis (can be enhanced with emoji names/keywords in the future)
export const searchEmojis = (query: string): string[] => {
  if (!query) return [];
  const allEmojis = getAllEmojis();
  // For now, just return all emojis if query exists
  // In the future, this can search by emoji names/keywords
  return allEmojis.filter((emoji) => emoji.includes(query));
};

